package com.returnsoft.collection.vo;

public class CreditCardFile {
	
	public String code;
	public String number;
	public String expirationDate;
	public String state;
	public String daysOfDefault;
	public String validation;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getExpirationDate() {
		return expirationDate;
	}
	public void setExpirationDate(String expirationDate) {
		this.expirationDate = expirationDate;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getDaysOfDefault() {
		return daysOfDefault;
	}
	public void setDaysOfDefault(String daysOfDefault) {
		this.daysOfDefault = daysOfDefault;
	}
	public String getValidation() {
		return validation;
	}
	public void setValidation(String validation) {
		this.validation = validation;
	}
	
	
	

}
