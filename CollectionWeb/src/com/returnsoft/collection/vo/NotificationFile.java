package com.returnsoft.collection.vo;

public class NotificationFile {
	
	private String orderNumber;
	private String nuicResponsible;
	private String correlativeNumber;
	private String state;
	private String sendingAt;
	private String answeringAt;
	private String reason;
	
	public String getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}
	public String getNuicResponsible() {
		return nuicResponsible;
	}
	public void setNuicResponsible(String nuicResponsible) {
		this.nuicResponsible = nuicResponsible;
	}
	public String getCorrelativeNumber() {
		return correlativeNumber;
	}
	public void setCorrelativeNumber(String correlativeNumber) {
		this.correlativeNumber = correlativeNumber;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getSendingAt() {
		return sendingAt;
	}
	public void setSendingAt(String sendingAt) {
		this.sendingAt = sendingAt;
	}
	public String getAnsweringAt() {
		return answeringAt;
	}
	public void setAnsweringAt(String answeringAt) {
		this.answeringAt = answeringAt;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
	
	
	

}
