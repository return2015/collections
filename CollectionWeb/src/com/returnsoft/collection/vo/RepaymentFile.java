package com.returnsoft.collection.vo;

public class RepaymentFile {
	
	private String code;
	private String insurancePremiumNumber;
	private String returnedAmount;
	private String returnedDate;
	private String paymentDate;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getInsurancePremiumNumber() {
		return insurancePremiumNumber;
	}
	public void setInsurancePremiumNumber(String insurancePremiumNumber) {
		this.insurancePremiumNumber = insurancePremiumNumber;
	}
	public String getReturnedAmount() {
		return returnedAmount;
	}
	public void setReturnedAmount(String returnedAmount) {
		this.returnedAmount = returnedAmount;
	}
	public String getReturnedDate() {
		return returnedDate;
	}
	public void setReturnedDate(String returnedDate) {
		this.returnedDate = returnedDate;
	}
	public String getPaymentDate() {
		return paymentDate;
	}
	public void setPaymentDate(String paymentDate) {
		this.paymentDate = paymentDate;
	}
	
	

}
